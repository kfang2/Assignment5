	<h3>Academic & Career</h3>
	<p>As long as I don’t fail any classes, I should be graduating this upcoming May. After that, I’m starting a software engineering position right outside of Boston. It’s funny that, somehow, I’ve made it through four years of college, stuck with my Computer Science major, and am now employed in a position that’ll apply my Computer Science knowledge! Along with my major, I am minoring in Political Science and have completed my cluster in American Sign Language. If time allowed it, I would be interested in minoring in ASL as well. I have two Deaf relatives, and I wish I was better able to communicate with them, so I'm glad I was able to learn more about Deaf culture through my cluster. </p>

	<h3>Extracurricular Activities</h3>
